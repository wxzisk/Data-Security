void keyschedule128(BYTE key128[11][4][4])
{
    // circulate byte left shift
    for (int rd = 1; rd < 11; rd++)
    {
        int p = 4;
        BYTE RotWord[4];
        int x = key128[rd - 1][3][0], m;

        for (m = 0; m < 4; m++)
        {
            RotWord[m] = key128[rd - 1][3][m];
        }

        for (m = 0; m < 3; m++)
        {
            RotWord[m] = RotWord[m + 1];
        }
        RotWord[3] = x;

        // subbyte
        BYTE subword[4];
        for (int i = 0; i < 4; i++)
        {
            subword[i] = SBOX[RotWord[i]];
        }

        // round step.
        subword[0] = (subword[0] ^ RCON[rd]);

        // xor
        for (int i = 0; i < 4; i++) //word 0 new key
        {
            key128[rd][0][i] = (key128[rd - 1][0][i] ^ subword[i]);
        }

        for (int t = 1; t < 4; t++)
        {

            for (int i = 0; i < 4; i++) //word 1, 2, 3 new key
            {
                key128[rd][t][i] = (key128[rd][t - 1][i] ^ key128[rd - 1][t][i]);
            }
        }
    }
    return;
}


void keyschedule192(BYTE key192[13][4][4], char *keystr)
{
    // 13 keys 0 to 12. need 52 words (0 to 51) but we could make 54 (1 + 8 iterations creation 6 words of 4)
    BYTE words[54][4];
    // roundkey 0.

    // init w0 to w5
    int count = 0;
    for (int i = 0; i < 6; i++)
    {
        for (int j = 0; j < 4; j++)
        {
            words[i][j] = keystr[count];
            count++;
        }
    }

    // now get words key schedule
    int i = 6;
    int rdc = 1;
    while (i < 54) //8 iterations
    {
        // circulate byte left shift
        BYTE rotword[4];

        // init rotword aka rotwordw6 = rot(w5)
        for (int m = 0; m < 4; m++)
        {
            rotword[m] = words[i - 1][m];
        }

        //circulate byte left shift on the rotword
        BYTE x = rotword[0];

        for (int m = 0; m < 3; m++)
        {
            rotword[m] = rotword[m + 1];
        }
        rotword[3] = x;

        // subbyte
        BYTE subword[4];
        for (int m = 0; m < 4; m++)
        {
            subword[m] = SBOX[rotword[m]];
        }

        // round step.
        subword[0] = subword[0] ^ RCON[rdc];

        // xor
        // new head :
        for (int m = 0; m < 4; m++)
        {
            words[i][m] = words[i - 6][m] ^ subword[m];
        }

        // 5 next words:
        for (int w = i + 1; w < i + 6; w++)
        {
            for (int m = 0; m < 4; m++)
            {
                words[w][m] = words[w - 6][m] ^ words[w - 1][m];
            }
        }

        i = i + 6; // + 6 words by iterations.
        rdc++;
    }


    // put words into key.
    int k = 0;

    for (int w = 0; w < 13; w++)
    {
        for (int v = 0; v < 4; v++)
        {
            for (int j = 0; j < 4; j++)
            {
                key192[w][v][j] = words[k][j];
            }
            k++;

        }
    }

    return;
}


